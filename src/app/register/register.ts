export class Register{
    firstName:any;
    lastName:any;
    email:any;
    password:any;
    dob:any;
    constructor(firstName:any,lastName:any,email:any,password:any,dob:any){
        this.firstName=firstName;
        this.lastName=lastName;
        this.email=email;
        this.password=password;
        this.dob=dob;
    }
}